								<p>
									La mayoría de los empresarios y el público en general desde algunos años piensan que el uso de las páginas web es como el uso de una tarjeta de presentación.
								</p>
								<p>
								Piénsalo un momento y trata de ver las cualidades de cada una. Una página web NO es una tarjeta de presentación y si alguien se refiere así a su propia web eso lo llevara a un posible fracaso porque no sabrá darle el valor y manejo que merecen sus atributos.
								</p>
								<h3>¿Por qué una página web no es una tarjeta de presentación?</h3>
								<ol>
									<li>Interacción: En la actualidad no surge aun una tarjeta de presentación que responda preguntas o pueda “hablar con el cliente”, y ciertamente no vende nada.</li>
									<li>Objetivo del visitante: Cuando un cliente entra a una web tiene un objetivo específico y muy concreto. Por ello se le tiene que dar al cliente exactamente lo que quiere. ¿Hay alguna tarjeta de presentación que atienda a cada cliente de una manera diferente?</li>
									<li>Conversión: La mayor capacidad que distingue una web es la capacidad de producir ventas y conseguir una base de datos para la empresa</li>
								</ol>
								<div class="ndheight"></div>
								<h3>¿Una web interactiva es más atractiva que un simple papel?</h3>
								<p>No es necesario dar más ejemplos, ya está más clara la diferencia. La página web no es una tarjeta de presentación. Ahora que tienes la información suficiente, vamos a darle más cuerpo a esta idea para tener un mejor entendimiento.</p>
								<div class="ndheight"></div>
								<h3>¿Qué es una página web? ¿Cuál es la analogía más adecuada para entenderlo?</h3>
								<p>La analogía más adecuada para una web es la de un “vendedor” o  activo digital, lo más conocido en inglés como “Salesman”. Llamemos a nuestro vendedor Danny. Danny trabaja en una tienda. Como en una página web vendedora, profesional y exitosa con un diseño de experiencia de usuario. Danny sabe quién es el cliente y dependiendo de eso el crea su estilo de hablar y vestir. Danny puede intuir que pasa por la mente del cliente en todo el proceso de venta hasta que el cliente hace finalmente la compra.</p>
								<p>Danny hace su trabajo tan bien que él sabe muy bien cuando el cliente puede perder la confianza en la compra. Por ejemplo, el cliente no sabe la calidad o el prestigio de la marca, no te preocupes, incluso antes de que el cliente pueda decir algo a Danny tiene ejemplos que muestran la calidad del producto, video que abalan su producto y testimoniales de clientes satisfechos.</p>
								<p>Después de ganar el corazón del cliente, le dio un sentido de seguridad sobre el producto y el mismo. Lo manejó intuitivamente para que saque su tarjeta de crédito. ¡Y listo, ahí tenemos otra venta!</p>
								<div class="ndheight"></div>
								<h3>¿Quieres un vendedor igual a Danny?</h3>
								<p>Todo lo que tienes que sabes es que tu página web es un vendedor con objetivo, que puede ser contactarte, vender, registrar, etc. Es decir, vender de forma diferente por cada rubro, mercado y manteniendo el mismo objetivo.</p>
								<p>En un mercado competitivo eso diferencia la marca y define si recuperas la inversión o no. Es como si Danny no era un buen vendedor, entonces no era rentable para la empresa. Poniendo en riesgo la existencia de la empresa.</p>
								<p>Es importante saber que el cambio de percepción de cómo es una página web es el primer paso y el más significativo para el éxito de cualquier negocio. Lo cual permitirá una plataforma funcional y rentable.</p>
								<div class="ndheight"></div>
